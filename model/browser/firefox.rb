require_relative 'browser'
module Browsers
  class Firefox < Browser
    class FirefoxException < StandardError
    end
    #----------------------------------------------------------------------------------------------------------------
    # class methods
    #----------------------------------------------------------------------------------------------------------------
    #----------------------------------------------------------------------------------------------------------------
    # instance methods
    #----------------------------------------------------------------------------------------------------------------
    def accept()
      "image/png,image/*;q=0.8,*/*;q=0.5"
    end

